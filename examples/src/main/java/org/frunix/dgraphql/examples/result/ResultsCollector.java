package org.frunix.dgraphql.examples.result;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.stereotype.Component;

import java.util.ArrayList;
import java.util.List;

@Component
public class ResultsCollector {

    private static final Logger log = LoggerFactory.getLogger(ResultsCollector.class);
    private final List<ExampleResult> results = new ArrayList<>();

    public void record(String example, String test, String query, String response, boolean success) {
        results.add(new ExampleResult(example, test, query, response, success));
    }

    public void printReport() {
        StringBuilder output = new StringBuilder();
        output.append(""
                + "\nв•”в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ"
                + "\nв•‘                           EXAMPLES REPORT"
                + "\nв• в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ");
        
        String currentSection = "";
        int passed = 0;
        int failed = 0;
        
        for (ExampleResult r : results) {
            if (!r.example().equals(currentSection)) {
                currentSection = r.example();
                output.append("\nв•‘");
                output.append("\nв•‘  %s".formatted(String.format("%-74s", currentSection)));
                output.append("\nв•‘");
            }
            
            String status = r.success() ? "вњ“ PASS" : "вњ— FAIL";
            if (r.success()) passed++; else failed++;
            
            output.append("\nв•‘    %s: %s".formatted(String.format("%-10s", status), String.format("%-60s", r.test())));
        }
        
        output.append("\nв•‘");
        output.append("\nв• в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ");
        output.append("\nв•‘  Total: %s | Passed: %s | Failed: %s".formatted( 
            String.format("%-6s", passed + failed),
            String.format("%-6s", passed),
            String.format("%-6s", failed)));
        output.append("\nв•љв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ");
        output.append("\n");
        log.info("{}", output);
    }

    public record ExampleResult(String example, String test, String query, String response, boolean success) {}
}
